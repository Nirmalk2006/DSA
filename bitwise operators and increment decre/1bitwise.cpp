#include <iostream>
using namespace std;

int main() {
    int a=10;
    int b=1;
    cout<<"a&b :" <<(a&b) <<endl;  // bitwise and
     cout<<"a|b :" <<(a|b) <<endl; // bitwise or
     cout<<"~a :" <<~a <<endl;  // bitwise not
     cout<<"a^b :" <<(a^b)<<endl;// bitwise xor
     cout<<"a<<b: "<<(a<<b)<<endl;// bitwise left shift
     cout<<"a>>b: "<< (a>>b)<<endl;// bitwise right shift
     
}